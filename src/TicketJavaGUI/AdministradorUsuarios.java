    private void btn_editUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editUsuarioActionPerformed
        String nombre = txt_nombreEditUsu.getText();
        String usuario = txt_usuarioEditUsu.getText();
        String contra = txt_contraEditUsu.getText();
        String edad = txt_edadEditUsu.getText();
        String u_buscar = txt_usuarioBuscUsu.getText();
        
        if(usuarios.modificarUsuario(u_buscar,nombre, usuario, contra, edad)){
            
            txt_nombreEditUsu.setText("");
            txt_usuarioEditUsu.setText("");
            txt_contraEditUsu.setText("");
            txt_edadEditUsu.setText("");
            txt_usuarioBuscUsu.setText("");
            JOptionPane.showMessageDialog(rootPane, "Usuario modificado satisfactoriamente!"
                        , "Administrador de Usuarios", WIDTH);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Usuario no existe!"
                        , "Error al Modificar", WIDTH);
        }
    }//GEN-LAST:event_btn_editUsuarioActionPerformed

    private void btn_buscUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscUsuarioActionPerformed
        String usuario = txt_usuarioBuscUsu.getText();
        usu = usuarios.buscarUsuario(usuario);
        if(usu != null){
            txt_edadEditUsu.setText(String.valueOf(usu.getEdad()));
            txt_nombreEditUsu.setText(usu.getNombre());
            txt_contraEditUsu.setText(usu.getPassword());
            txt_usuarioEditUsu.setText(usu.getUserName());
            int n = usuarios.getTipo(usu);
            switch (n) {
                case 0:
                    lbl_tipoEditUsu.setText("Usuario de Tipo Administrativo");
                    break;
                case 1:
                    lbl_tipoEditUsu.setText("Usuario de Tipo Contenido");
                    break;
                case 2:
                    lbl_tipoEditUsu.setText("Usuario de Tipo Limitado");
                    break;
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Usuario no existe!"
                        , "Error al Buscar", WIDTH);
        }
    }//GEN-LAST:event_btn_buscUsuarioActionPerformed

